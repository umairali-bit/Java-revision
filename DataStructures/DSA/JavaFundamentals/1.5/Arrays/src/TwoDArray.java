public class TwoDArray {

    public static void main(String[] args) {

        //two-dimensional array

        int marks[][] = {
                {12,98,34},
                {12,67,87},
                {12,98,23},
                {14,67,36}
        };

        System.out.println(marks[2][1]);
        System.out.println(marks[2][2]);



    }
}
