package dev.lpa.Operators;

public class Main {

    public static void main(String[] args) {

        //addition operator
        int a = 12;
        int b = 7;
    //    int result = a + b;
        System.out.println(a + b);

        //subtraction operator
        int c = 12;
        int d = 7;
        //    int result = c - d;
        System.out.println(c - d);

        //multiplication operator
        int e = 12;
        int f = 7;
        //    int result = e * f;
        System.out.println(e * f);

        //division operator
        int g = 12;
        int h = 7;
        //    int result = g / h;
        System.out.println((double) g / h);



    }
}
