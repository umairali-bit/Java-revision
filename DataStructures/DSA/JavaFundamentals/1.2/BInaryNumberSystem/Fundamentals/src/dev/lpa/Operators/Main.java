package dev.lpa.Operators;

public class Main {

    public static void main(String[] args) {

        //addition operator
        int a = 12;
        int b = 7;
    //    int result = a + b;
        System.out.println(a + b);

        //subtraction operator
        int c = 12;
        int d = 7;
        //    int result = c - d;
        System.out.println(c - d);

        //multiplication operator
        int e = 12;
        int f = 7;
        //    int result = e * f;
        System.out.println(e * f);

        //division operator
        int g = 12;
        int h = 7;
        //    int result = g / h;
        System.out.println((double) g / h);

        //modulus operator
        int i = 12;
        int j = 7;
        //    int result = i % j;
        System.out.println((double) i % j);


        //addition operator
        int k = 12;
        int l = 7;
        int result = k += l;
        System.out.println(result);

        //subtraction operator
        int m = 12;
        int n = 7;
        int results = m -= n;
        System.out.println(results);



    }
}
